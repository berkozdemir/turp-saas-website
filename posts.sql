## 5. Prostat Kanseri Hastaları İçin Turp Uygulaması: Entegre Çözüm

### Turp''ün Prostat Kanseri Hastalarına Özel Özellikleri

#### 1. e-Nabız Entegrasyonu: Komorbidite ve Medikasyon Takibi

Prostat kanseri hastalarının çoğu **yaşlı olup, ek hastalıkları** vardır (hipertansiyon, diyabet, kardiyovasküler hastalık). Turp, e-Nabız aracılığıyla:

- **Komorbidite Profili Otomatik Alınması**: Diyabet, kalp hastalığı, osteoporoz gibi ek hastalıklar otomatik olarak Turp''e yüklenir.
- **İlaç-İlaç Etkileşimi Analizi**: ADT, kemoterapeutikler, komorbidite ilaçları arasında etkileşim otomatik olarak kontrolür.
- **Kardiyovasküler Risk Takibi**: ED tedavisi (PDE-5 inhibitörleri), kardiyak riskli hastalarda özel uyarı ve kontrol mekanizmaları devreye girer.

#### 2. Prostat Spesifik PRO Anketleri

Turp''deki yerleşik PRO kütüphanesi, **prostat kanseri hastalarına özel anketleri** içerir:

**Haftalık İzleme Anketleri:**
- "Bu hafta sık idrara çıkma sorunu yaşadınız mı?" (1-10 şiddet ölçeği)
- "Gece kaç kez tuvalete gittiniz?" (objektif sayı)
- "İnkontinans yaşadınız mı ve ne kadar?" (pad sayısı)
- "Cinsel işlev hakkında şikayetiniz var mı?" (IIEF mini)
- "Genel enerji ve yorgunluk seviyeleri nasıl?" (0-10 ölçek)
- "Ruh durumunuz (depresyon/anksiyete) nasıl?" (PHQ-2)

**Aylık Kapsamlı Anketler:**
- Tam FACT-P yaşam kalitesi anketi
- Tam IIEF-15 erkek fonksiyonu anketi
- ICIQ-UI SF inkontinans anketi
- SF-36 genel yaşam kalitesi

#### 3. Akıllı Hatırlatma: Tedavi Uyumu ve Vizit Takibi

Prostat kanseri hastalarında, ADT uyumu kritiktir. Turp, bu noktada:

- **İlaç Alımı Hatırlatmaları**: Hastaya günlük olarak "ADT ilacınızı aldınız mı?" sorusu gönderilir. Cevaplar, adherence oranını hesaplar.
- **PSA Kontrol Randevusu Hatırlatmaları**: Başlıca PSA takibinin ne zaman yapılacağı, hastaya SMS ve push notification ile hatırlatılır. **No-show oranı %70 azalır.**
- **Dozaj Uyarıları**: Hastanın, dozajını kendi başına değiştirmesi durumunda (yan etki nedeniyle), Turp, doktor görüşmesi önerilir.

#### 4. Yan Etki Algılama ve Proaktif Müdahale

Turp, haftalık PRO verilerini analiz ederek:

- **ED Riski Öngörü**: IIEF puanı belirli bir seviyeye düşerse, sistem doktor ve hastaya uyarı gönderir. PDE-5 inhibitörü başlama önerilir.
- **İnkontinans Artışı**: Günlük pad sayısı artar ise, sistem üroloji konsultasyonu tavsiye eder.
- **Depresyon Belirtileri**: PHQ-2 puanı yükselse, psikiyatrik destek önerilir.
- **Osteoporoz Riski**: ADT kullanan hastaların, düzenli aralıklarla kemik yoğunluğu ölçümü (DEXA) alması önerilir, Turp bu takvimi hatırlatır.

#### 5. Hasta Eğitimi ve Kaynaklar

Turp, prostat kanserli hastaların uyum, self-management ve psikolojik destek için gerekli içeriği sağlar:

- **Videolar**: ADT yan etkileri, ED tedavisi, beslenme, egzersiz rehberleri
- **Makale Kütüphanesi**: Prostat kanseri, tedavi seçenekleri, yaşam stili değişiklikleri
- **Peer Support**: Benzer durumdaki diğer hastalarla bağlantı (anonim forum)

