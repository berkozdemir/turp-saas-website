#### 3. Akıllı Hatırlatma: Tedavi Uyumu ve Vizit Takibi

Meme kanseri hastalarında, kemoterapinin tamamlanması ve hormonal tedaviye uyumu kritiktir. Turp, bu noktada:

- **Kemoterapi Siklusu Hatırlatmaları**: Her kemoterapisi sırasında, hastaya vizit, kan testi, yan etki takibi hakkında hatırlatmalar gönderilir. **No-show oranı %70 azalır.**
- **Hormonal Tedavi Uyumu**: Tamoksifen, aromataz inhibitörleri günlük alım hatırlatmaları, uyum oranını artırır.
- **Kardiyovasküler Takip Hatırlatmaları**: Trastuzumab kullanan hastalar, periyodik echokardiografi için hatırlatılır.
- **Kemik Sağlığı Monitorlaması**: Aromataz inhibitörleri kullanan hastaların DEXA taraması ve osteoporoz tedavisi takip edilir.

#### 4. Yan Etki Algılama ve Proaktif Müdahale

Turp, haftalık PRO verilerini analiz ederek:

- **Lenfödem Erken Tespiti**: Kol çevresi artışı veya şişlik hissi başlarsa, derhal fiziksel terapist konsultasyonu önerilir, kompresyon giyim başlanır.
- **Nöropatik Ağrı Riski**: CIPN puanı artar ise, sistem nöroloji veya anestezi konsultasyonu tavsiye eder, pregabalin/gabapentin başlama önerilir.
- **Vasomotor Semptomlar**: Sıcak basması ve gece terlemesi artar ise, hormonal olmayan seçenekler (SSRI, venlafaxin) veya alternatif terapiler önerilir.
- **Cinsel İşlev Sorunları**: FSFI puanı düşerse, jinekoloji/seksolog konsultasyonu ve hormonal değerlendirme yapılır.
- **Body Image Disturbance**: BIS puanı yükselse, psikiyatristik destek ve varsa rekonstrüksiyon danışmanı önerilir.
- **Depresyon/Anksiyete Belirtileri**: PHQ-2 puanı yükselse, psikiyatrik destek önerilir, uygun ilaç tedavisi (SSRİ vb.) başlanabilir.
- **Kognitif Disfonksiyon**: Chemo brain semptomları tespit edilirse, nöropsikoloji değerlendirmesi, bilişsel rehabilitasyon önerilir.

