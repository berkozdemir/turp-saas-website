- **Hastalık Serbest Yaşam Süresi (Recurrence-Free Survival - RFS)**
- **Genel Yaşam Süresi (Overall Survival - OS)**
- **PSA Yanıt Oranı** (%50 ve üzeri PSA düşüşü)
- **Radyolojik Yanıt** (RECIST kriterlerine göre)

### Klinik Paradoks: İyi Objektif Sonuçlar, Kötü Yaşam Kalitesi

**Problem**: Pek çok prostat kanseri hastası, objektif olarak "başarılı" tedavi aldıktan sonra (hastalık remisyonda, PSA normal), aşağıdaki sorunlardan şikayetçidir:

