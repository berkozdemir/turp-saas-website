- **PRO Verileri**: Haftalık yaşam kalitesi, yan etki (diyaré, nötropeni, müaltin), uyum, persistans verileri
- **RWD ile Entegrasyon**: e-Nabız''dan alınan hormon durumu, TNM stage, komorbidite, kardiyovasküler durum verileriyle birleştirilir
- **Alt Grup Analizi**: Yaşa göre, menopoasal statüye göre, komorbiditeye göre, tedaviye uyuma göre çalışan gruplar
- **Yaşam Kalitesi Sonuçları**: Diyarenin yaşam kalitesini nasıl etkilediği, hormonal tedavinin vasomotor semptomları
