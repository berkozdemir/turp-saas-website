## 2. PRO''lar: Hasta Sesinin Doğrudan Kanıta Dönüşümü

### PRO Nedir ve Neden Önemlidir?

**PRO (Hasta Bildirimli Sonuç)**, hastanın sağlık durumu, semptomları ve hastalık veya tedavi ile ilgili işlevsel kapasitesi hakkındaki doğrudan algısının ölçümüdür. PRO''lar, ilacın hastanın günlük yaşamına, ruh haline ve genel refahına olan etkisini sayısal olarak nicelleştirir.

**ePRO (elektronik PRO)** ise mobil uygulamalar veya web arayüzleri aracılığıyla verinin anlık ve otomatik toplanmasını sağlar.

### PRO''ların RWE Çalışmalarında Kritik Rolü

**1. Hümanize Edilmiş Kanıt**

Regülatörler (FDA, EMA) ve ödeme kuruluşları, ilacın sadece biyolojik belirteçleri (biomarker) iyileştirmesinin ötesinde, **hastanın hayatını nasıl değiştirdiğini** görmek isterler. PRO verisi, tedavinin klinik faydasını hastanın perspektifinden sunar ve regülatif karar almada kritik rol oynar.

**2. Veri Bütünlüğü ve Gerçek Zamanlılık**

Özellikle kronik hastalıklarda, hastane vizitleri arasındaki dönemde yaşanan semptomlar sıklıkla unutulur veya yanlış hatırlanır (recall bias). ePRO platformları aracılığıyla veri, olayın hemen ardından toplanır, böylece hatırlama hataları en aza indirgenir.

**3. Heterojen Popülasyonun Derinlemesine Analizi**

RWE çalışmaları yüz binlerce hastayı içerebilir. PRO verisi bu geniş popülasyonda, hangi demografik grupların veya komorbiditesi olan hastaların tedaviden daha fazla fayda gördüğünü, hangi alt gruplarda yan etkilerin daha sık olduğunu anlamak için kritik bir analitik katman ekler.

