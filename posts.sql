## 1. Gerçek Yaşam Kanıtı (RWE) ve Geleneksel Verinin Sınırları

### RKÇ''den Gerçekliğe Geçiş

Geleneksel faz 3 RKÇ''ler, ilacın optimal koşullarda işe yarayıp yaramadığını gösterirken, hastaların aşağıdaki gerçeklikleri yeterince açıklayamaz:

- Birden fazla hastalığı (komorbiditeler) olan hastalar
- Karmaşık ilaç rejimlerine uyum sağlamaya çalışan hastalar
- Sosyal ve ekonomik farklılıkları olan hastalar
- Gerçek yaşam ortamlarında uzun vadede ilacı kullanan hastalar

RWE, bu eksiklikleri gidermek için tasarlanmıştır. Geniş heterojen popülasyonda toplanan veriler, ilacın veya tedavinin gerçek dünyadaki performansı hakkında kapsamlı bilgi sağlar.

### RWD''nin Kritik Boşlukları

Ancak **Gerçek Yaşam Verileri (RWD)**, doğası gereği hastanın **öznel deneyimi** ile ilgili önemli eksiklikler taşır:

**Semptom Şiddeti**
Bir EHR kaydı, hastanın ne zaman hastaneye gittiğini gösterir, ancak evde yaşadığı ağrı, yorgunluk veya bulantının şiddetini ve sürekliliğini kaydeder mi? Hayır.

**Yaşam Kalitesi (Quality of Life - QoL)**
Tedavi sonrası hastanın yaşam kalitesinde meydana gelen değişiklikler (uyku düzeni, sosyal aktiviteye katılım, günlük işlevsellik) geleneksel tıbbi kayıtlarda yer almaz.

**Tedaviye Uyum (Adherence ve Persistence)**
Hastanın reçeteyi düzenli kullanıp kullanmadığı veya tedavi rejimini ne kadar süreyle sürdürdüğü çoğunlukla belirsizdir.

**Subjektif Yan Etkiler**
Hafif veya orta şiddette yan etkiler (baş ağrısı, mide rahatsızlığı, enerji kaybı), doktor görüşmesinde unutulabilir veya ciddiye alınmayabilir.

İşte bu boşlukları dolduran tek güvenilir kaynak, hastanın kendisidir—yani **PRO''lardır**.

