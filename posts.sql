## 4. Prostat Kanseri RWE ve PRO''lar: Gerçek Dünyadaki Etkisizlik Problemi

### Klinik Uygulamada Karşılaşılan Paradoks

RCT''lerde, **androjen deprivasyon tedavisi (ADT)** kullanılan prostat kanseri hastalarında PSA yanıt oranı %80-90 olabilir. Ancak, gerçek dünyadaki pratikte:

- Hastalar reçeteyi **uygun şekilde kullanmayabilir** (hafta sonunda alma, hap sayısını azaltma)
- Yan etkiler (kilo alımı, osteoporoz, ED) nedeniyle **tedaviye uyum** azalabilir
- Sosyal ve ekonomik kısıtlamalar nedeniyle **persistans** yeterli olmayabilir

**RWE + PRO Çözümü**: Gerçek dünyadaki uyum ve persistans sorunları, PRO verileri sayesinde tespit edilebilir ve müdahale planlanabilir.

