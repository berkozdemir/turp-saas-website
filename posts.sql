## 4. Meme Kanseri RWE ve PRO''lar: Gerçek Dünyadaki Tedavi Uyumu

### Klinik Uygulamada Karşılaşılan Paradoks

RCT''lerde, **adjuvan kemoterapi** alan meme kanseri hastalarında tedaviye uyum oranı %85-95 olabilir. Ancak, gerçek dünyadaki pratikte:

- Hastalar yan etkileri nedeniyle tedaviye uyum göstermeyebilir (erken sonlandırma)
- Hormonal tedavi (tamoksifen, aromataz inhibitörleri) kullanan hastalar, yan etkiler nedeniyle uyumsuz kalabilir
- Lenfödem, nöropatik ağrı, ruh sağlığı sorunları hastaları desteğe ihtiyaç duyduğunu söylemekten alıkoyabilir

**RWE + PRO Çözümü**: Gerçek dünyadaki tedavi uyumu, yan etki sorunları ve yaşam kalitesi sonuçları, PRO verileri sayesinde tespit edilebilir ve müdahale planlanabilir.

