## 3. PRO''lar Prostat Kanseri Araştırmalarında Neden Kritiktir?

### Tedavi Seçimini Etkileyen Veriler

Prostat kanserinde, genellikle **birden fazla tedavi seçeneği** vardır (cerrahisi vs. radyoterapi vs. aktif izlem vs. kombinasyon). Aynı hastalık evresine sahip iki hasta için farklı tedaviler uygun olabilir. PRO verileri:

- **Bireyselleştirilmiş Karar Alma**: ED ve inkontinans açısından daha toleranslı bir hastaya daha agresif tedavi verilebilir; hassas hastalara konservatif yaklaşımlar sunulabilir.
- **Hasta Tercihi Tabanlı Tıp**: Hastanın yaşam kalitesi öncelikleri (çalışmaya dönme, cinsel yaşam, sosyal aktiviteler) doğrultusunda tedavi planlanması

### Yan Etkilerin Proaktif Yönetimi

Geleneksel İzlemde: Hasta 3-6 ayda doktor ziyaretinde yan etkilerden bahseder, ancak bu arada yaşanan sorunlar eksik kalabilir.

**PRO-Destekli Yaklaşımda**: Hastalar, Turp uygulaması üzerinden, haftada 1-2 kez ED, inkontinans, enerji seviyeleri hakkında sor edilirler. Sorunlar **erken tespiti** ve **proaktif müdahale** (örn. ED için PDE-5 inhibitörü başlangıcı) mümkün hale gelir.

### Regülatif Kabul Edilebilirlik

**FDA ve EMA**, son yıllarda prostat kanseri ilaçlarının onayında PRO verilerini çok ciddiye almaya başladı. Örneğin:

- **Enzalutamid** onayında, FACT-P yaşam kalitesi sonuçları sunulmuştur
- **Abiraterone** çalışmalarında, PSA yanıt kadar yaşam kalitesi sonuçları raporlanmıştır
- Yeni hormonal ajanlar ve kemoterapeutikler için PRO verileri, pazar erişimi ve geri ödeme kararlarında etkili olmaktadır

