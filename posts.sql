## 2. PRO''lar: Meme Kanserinde Hasta Deneyiminin Kuantitatif Ölçümü

### Meme Kanseri Bağlamında PRO''lar

**PRO (Hasta Bildirimli Sonuç)**, meme kanseri hastasının tedavi öncesi, sırası ve sonrasında yaşadığı semptomlar, yan etkiler, yaşam kalitesi, body image, cinsel işlev ve fonksiyonel durumunun **kendi doğrudan raporlaması**dır. Hiçbir hekim veya hemşirenin yorumu olmaksızın, hasta tarafından yapılan bu ölçümler, tedavinin gerçek etkisini gösterir.

### Meme Kanseri Spesifik PRO Ölçütleri

#### 1. **Yaşam Kalitesi Anketleri**

**EORTC QLQ-C30 + BR23 Modülü**
- Kanser hastalarında genel yaşam kalitesini ölçer (EORTC QLQ-C30)
- Meme kanseri spesifik yan etkileri ölçer (BR23 modülü): göğüs ağrısı, kol şişmesi, göğüs semptomları, sekonder malignite korkusu

**FACT-B (Functional Assessment of Cancer Therapy - Breast)**
- Meme kanseri hastasına özel tasarlanmış yaşam kalitesi anketi
- Fiziksel, sosyal, duygusal, fonksiyonel ve meme kanserine spesifik kaygıları ölçer
- 36 soru içerir ve kısa zamanda tamamlanabilir

**BREAST-Q (Breast Cancer Surgery and Outcomes)**
- Meme cerrahisi sonrası beden memnuniyeti ve göğüs rekonstruksiyon memnuniyetini ölçer
- Cosmetic outcomes ve patient satisfaction odaklı
- Implant vs. autologous rekonstruksiyon seçimlerinde rehber olur

**SF-36 (Short Form 36)**
- Genel sağlık durumu ve yaşam kalitesi ölçer
- Fiziksel ve mental bileşenleri değerlendirir

#### 2. **Lenfödem ve Kolda Fonksiyon Ölçütleri**

**Lenfödem Yağıltı Ölçeği (LYMPH-Q)**
- Kolda şişme, sertlik, belirtiler ve işlevsellik ölçer
- Eğer varsa lenfödemaya tedaviye yanıt değerlendirir

**Kol Edema Seviyesi Ölçeği (Circumference Measurement + Bioimpedance)**
- Objektif lenfödem ölçümü (santimetre, mL hacim)
- PRO ile birleşince, hasta raporlanan ve objektif verinin uyumu değerlendirilir

#### 3. **Nöropati ve Ağrı Ölçütleri**

**EORTC QLQ-CIPN20 (Chemotherapy-Induced Peripheral Neuropathy)**
- Kemoterapi ilişkili periferik nöropatinin şiddetini ölçer
- Sensory, motor, autonomic semptomları ve etkilenen fonksiyonları değerlendirir

**Neuropathic Pain Scale (NPS)**
- Post-mastektomi ağrı sendromu (PMPS) şiddetini ölçer
- Ağrının lokasyonu, karakteri, etkili faktörler

**Brief Pain Inventory (BPI)**
- Ağrı şiddeti ve yaşam kalitesine etkisini ölçer

#### 4. **Body Image ve Cinsellik Ölçütleri**

**Body Image Scale (BIS)**
- Tedavi sonrası beden memnuniyeti ve görünüş endişesi
- Giyim seçimleri ve sosyal aktivitelere etkileri

**Female Sexual Function Index (FSFI)**
- Cinsel işlev, lubrikasyon, orgasm, memnuniyet değerlendirilir
- Hormonal tedavi ve kemoterapi sonrası cinsel sorunları ölçer

**Relationship and Sexuality Questionnaire (RSQ)**
- İlişkiye etkileri, cinsel memnuniyeti, partnership dinamikleri

#### 5. **Kognitif Fonksiyon ve Kemobrain Ölçütleri**

**FACT-Cog (Cognitive Function Module)**
- Kemoterapiye bağlı kognitif disfonksiyonu (chemo fog) ölçer
- Bellek, konsantrasyon, sözel akıcılık, zihinsel gevşeklik

**Montreal Cognitive Assessment (MoCA)**
- Objektif kognitif test (objektif + PRO kombinasyonu)

#### 6. **Psikososyal Sonuçlar**

**Depresyon ve Anksiyete Ölçeği (PHQ-9, GAD-7)**
- Meme kanseri tanısı ve tedavisi sonrası ruh sağlığı takip edilir
- Prognostik faktör olarak davranır

**Impact of Event Scale (IES)**
- Kanser tanısının travmatik etkisi ve başa çıkma mekanizmaları

