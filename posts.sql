## 2. PRO''lar: Prostat Kanserinde Hasta Deneyiminin Kuantitatif Ölçümü

### Prostat Kanseri Bağlamında PRO''lar

**PRO (Hasta Bildirimli Sonuç)**, prostat kanseri hastasının tedavi öncesi, sırası ve sonrasında yaşadığı semptomlar, yan etkiler, yaşam kalitesi ve fonksiyonel durumunun **kendi doğrudan raporlaması**dır. Hiçbir hekim veya hemşirenin yorumu olmaksızın, hasta tarafından yapılan bu ölçümler, tedavinin gerçek etkisini gösterir.

### Prostat Kanseri Spesifik PRO Ölçütleri

#### 1. **Yaşam Kalitesi Anketleri**

**EORTC QLQ-C30 + PR25 Modülü**
- Kanser hastalarında genel yaşam kalitesini ölçer (EORTC QLQ-C30)
- Prostat spesifik yan etkileri ölçer (PR25 modülü): ED, inkontinans, gastrointestinal semptomlar

**FACT-P (Functional Assessment of Cancer Therapy - Prostate)**
- Prostat kanseri hastasına özel tasarlanmış yaşam kalitesi anketi
- Fiziksel, sosyal, duygusal ve fonksiyonel boyutları ölçer
- 39 soru içerir ve kısa zamanda tamamlanabilir

**SF-36 (Short Form 36)**
- Genel sağlık durumu ve yaşam kalitesi ölçer
- Fiziksel ve mental bileşenleri değerlendirir

#### 2. **Fonksiyonel Başarısızlık Ölçütleri**

**Eritillik Fonksiyon Ölçeği (IIEF - International Index of Erectile Function)**
- 15 soru, erkek cinsel işlevi ve memnuniyeti ölçer
- Prostat kanseri tedavisi sonrası ED''nin şiddetini belirler
- Tedavi müdahalelerinin (PDE-5 inhibitörleri, intrakorporeal enjeksiyon) etkinliğini değerlendirir

**Uriner İnkontinans Ölçeği (ICIQ-UI SF - Incontinence Severity Index)**
- İnkontinansın şiddeti ve yaşam kalitesi etkisini ölçer
- Hasta tarafından raporlanan günlük kaçak idrar miktarı ve sosyal etkileri değerlendirir

**Gastrointestinal Semptom Ölçeği (EORTC QLQ-C30''daki GI modülü)**
- Diyaré, kusma, ishal sonrası sorunlar ölçer
- Radyoterapi sonrası gastrointestinal toksisite izlenir

#### 3. **Psikososyal Sonuçlar**

**Depresyon ve Anksiyete Ölçeği (PHQ-9, GAD-7)**
- Prostat kanseri tanısı ve tedavisi sonrası ruh sağlığı takip edilir
- Prognostik faktör olarak davranır (depresif hastalar daha kötü uyum gösterir)

**Beden İmajı ve Cinsellik Ölçeği (Body Image Scale)**
- Tedavi sonrası beden algısındaki değişiklikleri ve cinsel kimliğe etkileri ölçer

