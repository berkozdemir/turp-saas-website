## 3. PRO''lar Meme Kanseri Araştırmalarında Neden Kritiktir?

### Tedavi Seçimini Etkileyen Veriler

Meme kanserinde, genellikle **birden fazla tedavi seçeneği** vardır (lumpektomi vs. mastektomi, rekonstrüksiyon seçenekleri, neoadjuvan vs. adjuvan kemoterapi). Benzer hastalık evresine sahip iki hasta için farklı tedaviler uygun olabilir. PRO verileri:

- **Bireyselleştirilmiş Karar Alma**: Beden imajından çok endişeli bir hastaya mastektomi + rekonstrüksiyon; estetik sonuçtan daha çok işlevselliği önemseyen hastaya lumpektomi sunulabilir.
- **Hasta Tercihi Tabanlı Tıp**: Hastanın yaşam kalitesi öncelikleri (kariyeri, çocuk sahibi olma, cinsel yaşam) doğrultusunda tedavi planlanması

### Yan Etkilerin Proaktif Yönetimi

Geleneksel İzlemde: Hasta 3-6 ayda onkoloğu ziyaretinde yan etkilerden bahseder, ancak bu arada yaşanan sorunlar eksik kalabilir.

**PRO-Destekli Yaklaşımda**: Hastalar, Turp uygulaması üzerinden, haftada 1-2 kez lenfödem, nöropatik ağrı, vasomotor semptomlar, cinsel işlev, body image hakkında sor edilirler. Sorunlar **erken tespiti** ve **proaktif müdahale** (örn. lenfödem fizyoterapisi, nöropatik ağrı tedavisi, psikiyatrik destek) mümkün hale gelir.

### Regülatif Kabul Edilebilirlik

**FDA ve EMA**, son yıllarda meme kanseri ilaçlarının onayında PRO verilerini çok ciddiye almaya başladı. Örneğin:

- **HER2-pozitif hastalarda trastuzumab** onayında, yaşam kalitesi sonuçları sunulmuştur
- **CDK4/6 inhibitörleri (palbociclib, ribociclib)** çalışmalarında, yaşam kalitesi verisi regülatif kararı etkilemiştir
- Yeni hormon tedavileri ve targeted ajanlar için PRO verileri, pazar erişimi ve geri ödeme kararlarında etkili olmaktadır

