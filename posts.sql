## 5. Meme Kanseri Hastaları İçin Turp Uygulaması: Entegre Çözüm

### Turp''ün Meme Kanseri Hastalarına Özel Özellikleri

#### 1. e-Nabız Entegrasyonu: Komorbidite ve Medikasyon Takibi

Meme kanseri hastalarının çoğu **yaşlı olup, ek hastalıkları** vardır (hipertansiyon, diyabet, kardiyovasküler hastalık, osteoporoz). Turp, e-Nabız aracılığıyla:

- **Komorbidite Profili Otomatik Alınması**: Osteoporoz, kardiyovasküler hastalık, diyabet gibi ek hastalıklar otomatik olarak Turp''e yüklenir.
- **İlaç-İlaç Etkileşimi Analizi**: Kemoterapi, hormonal tedavi, ek ilaçlar arasında etkileşim otomatik olarak kontrolür.
- **Kardiyovasküler Risk Takibi**: Trastuzumab kullanan hastaların kardiyotoksisitesi, echokardiografi takibi otomatik hatırlatılır.
- **Kemik Sağlığı Monitorlaması**: Aromataz inhibitörleri kullanan hastaların DEXA taraması ve kalsiyum/D vitamini kullanımı takip edilir.

#### 2. Meme Kanseri Spesifik PRO Anketleri

Turp''deki yerleşik PRO kütüphanesi, **meme kanseri hastalarına özel anketleri** içerir:

**Haftalık İzleme Anketleri:**
- "Bu hafta kol veya göğüs alanında şişlik hissettiniz mi?" (lenfödem taraması, 1-10 ölçek)
- "Kol veya el kuvvetinde azalma var mı?" (fonksiyonel değerlendirme)
- "Nöropatik ağrı (iğnelenme, yanma, uyuşukluk) yaşadınız mı?" (CIPN taraması, lokasyon)
- "Sıcak basması veya gece terlemesi yaşadınız mı?" (vasomotor semptomlar, sıklık)
- "Cinsel işlev hakkında sorun var mı?" (FSFI mini)
- "Beden görünüşü hakkında kaygı var mı?" (body image, 0-10 ölçek)
- "Ruh durumunuz (depresyon/anksiyete) nasıl?" (PHQ-2)
- "Konsantrasyon ve bellek sorunu yaşadınız mı?" (chemo brain taraması)

**Aylık Kapsamlı Anketler:**
- Tam FACT-B yaşam kalitesi anketi
- Tam FSFI cinsel işlev anketi
- BIS body image anketi
