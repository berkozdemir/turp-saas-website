## 3. PRO ve RWE Sinerjisi: Değer Zincirinde Dönüşüm

### Tedavi Uyumu (Adherence) ve Persistansın Ölçülmesi

RWE çalışmalarının başarısı, hastanın tedaviye ne kadar süreyle devam ettiğine (persistence) ve ne kadar düzenli kullandığına (adherence) doğrudan bağlıdır.

**Geleneksel RWD Sorunu**: Eczane kayıtları ilaçların satın alındığını gösterebilir, ancak hastanın gerçekten kullanıp kullanmadığını göstermez. Hastane vizitleri ise seyrek olduğundan, bu arada geçen zamanı kapsayamaz.

**PRO Çözümü**: Dijital PRO platformları aracılığıyla:
- Hastalar mobil uygulama üzerinden ilaç alımını teyit edebilir
- Sistem, vizit hatırlatmaları göndererek hastane erişimlerindeki kaçış oranlarını düşürür
- Hastanın kendi raporladığı yan etkiler, tedaviye devam etme kararını etkileyen faktörleri ortaya koymaktadır

### Veri Kalitesi ve Doğrulanabilirlik

PRO verisini RWE''ye entegre ederken veri güvenilirliğini sağlamak, kritik bir zorluktur. Modern çözümler şunları içerir:

**Kimlik Doğrulama (KYC)**
Hastanın kimliğinin ulusal sağlık sistemleri üzerinden doğrulanması, toplanan PRO verisinin gerçek ve izlenebilir bir kaynaktan geldiğini garantiye almaktadır. Bu, regülatörler için verinin güvenilirliğini ve kabul edilebilirliğini katlanarak artırır.

**Medikasyon Geçmişi Doğrulaması**
PRO''larla toplanan semptom ve yan etki verileri, hastanın geçmişteki komorbiditeler ve eşlik eden ilaçlar ile çapraz kontrol edilir. Bu, yanlılık riskini (bias) minimize eder ve sadece dijital entegrasyonla mümkündür.

**Temporal Tutarlılık**
Veri toplama hızı (gün cinsinden) arttıkça, verinin doğruluğu ve değeri artar. Aylık anketler yerine günlük ePRO uyarıları, hastaların hatırlama yanlılığını önemli ölçüde azaltır.

### Stratejik Karar Almada PRO''ların Etkisi

PRO destekli RWE verisi, ilaç şirketlerinin ve ödeme kuruluşlarının karar süreçlerini doğrudan etkiler:

**Pazar Erişimi ve Ödeme Kararları**

Ödeme kuruluşları (reimbursement bodies), ilacın sadece klinik etkinliğinin ötesinde, **hastanın yaşam kalitesini ne kadar iyileştirdiğini** görmek isterler. PRO verisi, ilacın fiyatlandırılması ve geri ödeme kapsamı kararları için vazgeçilmez argüman sunar.

**Farmakovijilans ve Güvenlik**

İlaç piyasaya sürüldükten sonra (Post-Marketing Surveillance), nadir görülen veya hastalar tarafından önemsenmeyip rapor edilmeyen yan etkiler, PRO araçları aracılığıyla hızlı bir şekilde toplanabilir.

**Etiketleme ve Hak Talepleri**

PRO verisi, ilacın ambalaj etiketine "Yaşam Kalitesini İyileştirir" veya "Ağrıyı %X Oranında Azaltır" gibi yeni iddiaların eklenmesi için güçlü bir kanıt sunabilir.

