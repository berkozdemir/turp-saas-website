## 6. Meme Kanseri Araştırmalarında Turp Destekli PRO''lar: Pratik Uygulamalar

### Klinisyen Perspektifi: Tedavi Planlaması

**Senaryo**: 55 yaşında, HER2-pozitif meme kanseri tanısı alan hastaya adjuvan kemoterapi (trastuzumab) başlanılıyor.

**Turp Olmadan**:
- Hasta, 3 ayda onkoloğu ziyaretinde yan etkilerden bahseder
- Onkolog, lenfödem başladığını veya nöropatik ağrı olduğunu öğrenir (bu arada 3 ay boyunca hastanın semptomları şiddetlendi)
- Body image disturbance ve depresyon belirtileri kaçırılabilir
- Hormonal tedaviye başlandıktan sonra vasomotor semptomlar yaşansa bile, aylarca rapor edilmez

**Turp İle**:
- Hafta 1: Kemoterapi başına yan etkileri monitorlanmaya başlanır, terapötik rehber sağlanır
- Hafta 2: Lenfödem belirtileri tespit edilir, fizyoterapist konsultasyonu önerilir
- Hafta 3: İlk nöropatik ağrı işareti algılanır, nöroloji konsultasyonu + pregabalin önerilir
- Hafta 4: Body image ve depresyon belirtileri görülür, psikolog başvurusu önerilir
- Ay 3: Trastuzumab müdahalesine karşı kardiyotokkisitesi ekokardiografi ile takip edilir
- Devam: Aylık PRO raporu oluşturulur, yan etki yönetimi proaktif hale gelir

**Sonuç**: Hasta, yaşam kalitesi kaybı minimize edilmiş şekilde tedaviyi tamamlar.

### Araştırma Perspektivi: Meme Kanseri RWE Çalışmaları

**CDK4/6 İnhibitörünün Gerçek Dünyadaki Etkinliği Çalışması**:

