## 1. Meme Kanseri Tedavisi ve Geleneksel Başarı Kriterlerinin Yetersizliği

### Geleneksel Tedavi Sonuç Ölçütleri

Meme kanseri araştırmalarında, uzun yıldır aşağıdaki objektif parametreler başarı göstergesi olarak kullanılmıştır:

- **Hastalık Serbest Yaşam Süresi (Disease-Free Survival - DFS)**
- **Genel Yaşam Süresi (Overall Survival - OS)**
- **Tümör Yanıt Oranı (Response Rate - RR)** (%50 ve üzeri tümör küçülmesi)
- **Patolojik Tam Yanıt (Pathological Complete Response - pCR)**

### Klinik Paradoks: İyi Objektif Sonuçlar, Önemli Yaşam Kalitesi Sorunları

**Problem**: Pek çok meme kanseri hastası, objektif olarak "başarılı" tedavi aldıktan sonra (pCR, hastalık remisyonda, sağkalım iyileşmiş), aşağıdaki sorunlardan şikayetçidir:

- **Lenfedema**: Cerrahi ve radyoterapiden sonra %20-30 oranında; kronik, progresif ve yaşam boyunca devam eden sorun
- **Nöropatik Ağrı (Post-Mastectomy Pain Syndrome - PMPS)**: Cerrahi sonrası %25-60 oranında; sıklıkla kronik ve tedaviye dirençli
- **Kemo-İlişkili Kognitif Disfonksiyon (Chemo Brain/Chemo Fog)**: Kemoterapiyi alan hastaların %70''inde rapor edilen bellek ve konsantrasyon sorunları
- **Vasomotor Semptomlar**: Tamoksifen, aromataz inhibitörleri kullanan pre-menoposal kadınlarda sıcak basması ve gece terlemesi
- **Görünüş Değişiklikleri ve Body Image**: Mastektomi veya lumpektomi sonrası asimetri, dövme/tatuaj ihtiyacı, protez uyumu
- **Cinsel İşlev Bozukluğu**: Hormonal tedavi ve kemoterapi nedeniyle libido kaybı, vajinal kuruluk, dyspareunia
- **Ruh Sağlığı**: Depresyon, anksiyete, travma sonrası stres bozukluğu (PTSD), sosyal izolasyon

**Geleneksel Yaklaşımdaki Boşluk**: Klasik RCT''ler, bu yaşam kalitesi sorunlarını yeterince ölçmez ve raporlamaz. Klinik çıktılar sadece "remisyon", "sağkalım" ile sınırlı kalabilir, oysa hastanın günlük yaşamı önemli ölçüde etkilenmiş olabilir.

