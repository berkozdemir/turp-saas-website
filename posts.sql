- **Uriner İnkontinans**: Ameliyat sonrası %5-20 oranında persisten inkontinans meydana gelir.
- **Gastrointestinal Yan Etkiler**: Radyoterapi sonrası ishaal, anorektal sorunlar kronik olarak devam edebilir.
- **Androjen Deprivasyon Yan Etkileri**: ADT kullanan hastalar, kilo alımı, osteoporoz, yorgunluk, kognitif düşüş yaşayabilir.
- **Ruh Sağlığı**: Anksiyete, depresyon, sosyal izolasyon sıklıkla gözlenebilir.

**Geleneksel Yaklaşımdaki Boşluk**: Klasik RCT''ler, bu yaşam kalitesi sorunlarını yeterince ölçmez ve raporlamaz. Klinik çıktılar sadece "remisyon", "sağkalım" ile sınırlı kalabilir, oysa hastanın günlük yaşamı önemli ölçüde etkilenmiş olabilir.

